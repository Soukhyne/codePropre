package ex1;

/** Classe qui effectue des opérations mathématique sur des chiffres
 * @author Madeline
 *
 */
public class Calcul {

	/**
	 * @param a a est un entier
	 * @param b b est un entier
	 * @return int résultat de l'addition
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * @param a a est un entier
	 * @param b b est un entier
	 * @return int résultat de la soustraction
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
